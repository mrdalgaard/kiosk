-- Views

CREATE OR REPLACE VIEW public.todayssalesgrouped AS
 SELECT customers.customername,
    sales.soldproductname,
    sum(sales.quantity) AS sum
   FROM sales
     JOIN customers USING (customerid)
  WHERE CAST(sales."timestamp" AS date) = CURRENT_DATE
  GROUP BY sales.soldproductname, customers.customername
  ORDER BY (sum(sales.quantity)) DESC;

CREATE OR REPLACE VIEW public.purchasehistory AS
 SELECT sales.id,
    to_char((date_trunc('minute'::text, sales."timestamp") AT TIME ZONE CURRENT_SETTING('TIMEZONE')), 'DD/MM-YYYY HH24:MI'::text) AS "time",
    sales.quantity,
    sales.soldsum AS salessum,
    customers.customerid,
    customers.customername,
    sales.soldproductname AS productname,
    sales."timestamp"
   FROM sales
     JOIN customers USING (customerid)
  ORDER BY sales.id;

CREATE OR REPLACE VIEW public.lastmowed AS
 SELECT days,
    customername,
    section_name
   FROM ( SELECT DISTINCT ON (s.section_name) date_part('days'::text, now() - m.timestamp)::integer AS days,
            c.customername,
            s.section_name
           FROM mowingactivities m
             JOIN mowingsections s ON m.section_id = s.id
             JOIN customers c ON m.user_id = c.customerid
          WHERE m.status::text = '8/8'::text
          ORDER BY s.section_name, m.timestamp DESC) test
  ORDER BY days DESC;

CREATE OR REPLACE VIEW public.mowinghistory AS
 SELECT to_char(m.timestamp::date, 'dd/mm/yyyy'::text) AS date,
    c.customername,
    s.section_name,
    m.status
   FROM mowingactivities m
     JOIN mowingsections s ON m.section_id = s.id
     JOIN customers c ON m.user_id = c.customerid
  ORDER BY m.id DESC;

CREATE OR REPLACE VIEW public.maintenancestatus AS
 SELECT m.id,
    m.maintenance_type,
    m.interval_h,
    m.last_maintained_timestamp,
    c.customername AS maintained_by,
    COALESCE(SUM(
        s.cutting_time_in_h *
        CAST(SPLIT_PART(a.status, '/', 1) AS FLOAT) /
        CAST(COALESCE(NULLIF(SPLIT_PART(a.status, '/', 2), ''), '8') AS FLOAT)
    ), 0) AS used_h,
    m.interval_h - COALESCE(SUM(
        s.cutting_time_in_h *
        CAST(SPLIT_PART(a.status, '/', 1) AS FLOAT) /
        CAST(COALESCE(NULLIF(SPLIT_PART(a.status, '/', 2), ''), '8') AS FLOAT)
    ), 0) AS remaining_h
   FROM mowingmaintenance m
     LEFT JOIN mowingactivities a ON a.timestamp > m.last_maintained_timestamp
     LEFT JOIN mowingsections s ON a.section_id = s.id AND a.status != 'NotMowed'
     LEFT JOIN customers c ON m.user_id = c.customerid
  GROUP BY m.id, m.maintenance_type, m.interval_h, m.last_maintained_timestamp, c.customername
  ORDER BY m.id;
