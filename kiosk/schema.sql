-- Tables

CREATE TABLE IF NOT EXISTS public.schema_migrations
(
    id SERIAL PRIMARY KEY,
    version VARCHAR(255) NOT NULL UNIQUE,
    applied_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS public.customers
(
    customerid integer NOT NULL,
    customername text COLLATE pg_catalog."default" NOT NULL,
    deleted boolean NOT NULL DEFAULT false,
    customergroup smallint,
    CONSTRAINT "Customer_pkey" PRIMARY KEY (customerid)
);

CREATE TABLE IF NOT EXISTS public.products
(
    productid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 9 ),
    productname text COLLATE pg_catalog."default" NOT NULL,
    itemprice numeric(8, 2) NOT NULL,
    imagefilename text COLLATE pg_catalog."default" NOT NULL,
    disabled boolean NOT NULL DEFAULT false,
    sorting smallint DEFAULT 100,
    CONSTRAINT "Products_pkey" PRIMARY KEY (productid)
);

CREATE TABLE IF NOT EXISTS public.sales
(
    id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "timestamp" timestamp with time zone NOT NULL DEFAULT now(),
    customerid integer NOT NULL,
    productid integer NOT NULL,
    quantity integer NOT NULL,
    solditemprice numeric(8, 2) NOT NULL,
    soldproductname text COLLATE pg_catalog."default",
    soldsum numeric(8, 2) GENERATED ALWAYS AS ((solditemprice * (quantity)::numeric)) STORED,
    CONSTRAINT "Sales_pkey" PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS public.ecotransfer
(
    salesid integer NOT NULL,
    "timestamp" timestamp with time zone NOT NULL DEFAULT now(),
    preupdatesum numeric(8, 2),
    attempts smallint DEFAULT 0,
    transferred boolean NOT NULL DEFAULT false,
    errormsg text COLLATE pg_catalog."default"
);

CREATE TABLE IF NOT EXISTS public.mowingsections
(
    id serial NOT NULL,
    section_name character varying(50) COLLATE pg_catalog."default" NOT NULL,
    cutting_time_in_h real NOT NULL DEFAULT 0,
    disabled boolean NOT NULL DEFAULT false,
    CONSTRAINT mowingsections_pkey PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS public.mowingusers
(
    customerid integer NOT NULL,
    CONSTRAINT mowingusers_pkey PRIMARY KEY (customerid)
);

CREATE TABLE IF NOT EXISTS public.mowingactivities
(
    id serial NOT NULL,
    user_id integer NOT NULL,
    timestamp timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    section_id integer NOT NULL,
    status character varying(20) COLLATE pg_catalog."default" NOT NULL,
    CONSTRAINT mowingactivities_pkey PRIMARY KEY (id),
    CONSTRAINT mowingactivities_section_id_fkey FOREIGN KEY (section_id)
        REFERENCES public.mowingsections (id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT mowingactivities_user_id_fkey FOREIGN KEY (user_id)
        REFERENCES public.customers (customerid) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
);

CREATE TABLE IF NOT EXISTS public.mowingmaintenance
(
    id smallserial NOT NULL,
    maintenance_type character varying(50) COLLATE pg_catalog."default" NOT NULL,
    interval_h real NOT NULL,
    last_maintained_timestamp timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    user_id integer,
    CONSTRAINT mowingmaintenance_pkey PRIMARY KEY (id),
    CONSTRAINT mowingmaintenance_user_id_fkey FOREIGN KEY (user_id) REFERENCES public.customers(customerid)
);

